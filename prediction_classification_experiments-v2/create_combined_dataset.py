def filter_by_domain(df, domain_name):
    """
    Filter dataset by domain column.
    
    Parameters
    ----------
    df : pd.DataFrame
        Dataset with 'Domain' column
    domain_name : str
        Domain to filter for (e.g., 'finance', 'weather')
    
    Returns
    -------
    pd.DataFrame
        Filtered dataset
    """
    print(f"\nFiltering for domain: '{domain_name}'")
    
    if 'Domain' not in df.columns:
        print(f"â ï¸  Warning: 'Domain' column not found. Skipping filter.")
        return df
    
    original_len = len(df)
    filtered_df = df[df['Domain'] == domain_name]
    filtered_len = len(filtered_df)
    
    print(f"  Original size: {original_len}")
    print(f"  Filtered size: {filtered_len}")
    print(f"  Kept {filtered_len/original_len*100:.1f}% of rows")
    
    return filtered_df

def combine_datasets(dataset_list, dataset_names):
    """
    Combine multiple datasets with standard columns.
    
    Parameters
    ----------
    dataset_list : list of pd.DataFrame
        List of datasets to combine
    dataset_names : list of str
        Names of datasets for display
    
    Returns
    -------
    pd.DataFrame
        Combined dataset with standard columns
    
    Notes
    -----
    Standard columns: ['Base Sentence', 'Sentence Label']
    Additional columns from individual datasets are preserved but may
    contain NaN values for datasets that don't have them.
    """
    print("\n" + "="*60)
    print("COMBINE DATASETS")
    print("="*60)
    
    print(f"Combining {len(dataset_list)} datasets:")
    for name, df in zip(dataset_names, dataset_list):
        print(f"  - {name}: {len(df)} rows")
    
    # Combine all datasets
    combined_df = DataProcessing.concat_dfs(dataset_list)
    
    print(f"\nâ Combined dataset shape: {combined_df.shape}")
    print(f"Columns: {list(combined_df.columns)}")
    
    # Check for required columns
    required_cols = ['Base Sentence', 'Sentence Label']
    missing_cols = [col for col in required_cols if col not in combined_df.columns]
    
    if missing_cols:
        print(f"\nâ ï¸  Warning: Missing required columns: {missing_cols}")
    else:
        print(f"\nâ All required columns present: {required_cols}")
    
    # Show label distribution
    if 'Sentence Label' in combined_df.columns:
        print(f"\nSentence Label distribution:")
        print(combined_df['Sentence Label'].value_counts())
    
    print(f"\nPreview:\n{combined_df.head(3)}")
    print(f"\nTail:\n{combined_df.tail(3)}\n")
    
    return combined_df

def extract_standard_columns(combined_df, additional_cols=None):
    """
    Extract standard columns plus any additional specified columns.
    
    Parameters
    ----------
    combined_df : pd.DataFrame
        Full combined dataset
    additional_cols : list of str, optional
        Additional columns to keep beyond standard ones
    
    Returns
    -------
    pd.DataFrame
        Dataset with selected columns only
    """
    standard_cols = ['Base Sentence', 'Sentence Label']
    
    if additional_cols:
        keep_cols = standard_cols + additional_cols
    else:
        keep_cols = standard_cols
    
    # Only keep columns that exist
    keep_cols = [col for col in keep_cols if col in combined_df.columns]
    
    print(f"\nExtracting columns: {keep_cols}")
    extracted_df = combined_df.loc[:, keep_cols]
    
    return extracted_df

def save_combined_dataset(df, save_path, output_name, include_version=True):
    """
    Save combined dataset to disk.
    
    Parameters
    ----------
    df : pd.DataFrame
        Dataset to save
    save_path : str
        Directory path for saving
    output_name : str
        Base filename (without extension)
    include_version : bool
        Whether to auto-increment version number if file exists
    """
    print("\n" + "="*60)
    print("SAVE COMBINED DATASET")
    print("="*60)
    
    os.makedirs(save_path, exist_ok=True)
    
    DataProcessing.save_to_file(
        df, 
        save_path, 
        output_name, 
        'csv',
        include_version=include_version
    )
    
    # Find the actual saved filename
    if include_version:
        # Check for versioned files
        existing_files = [f for f in os.listdir(save_path) if f.startswith(output_name)]
        if existing_files:
            latest_file = sorted(existing_files)[-1]
            full_path = os.path.join(save_path, latest_file)
        else:
            full_path = os.path.join(save_path, f"{output_name}.csv")
    else:
        full_path = os.path.join(save_path, f"{output_name}.csv")
    
    print(f"\nâ Saved dataset:")
    print(f"  Path: {full_path}")
    print(f"  Shape: {df.shape}")
    print(f"  Size: {os.path.getsize(full_path) / 1024:.2f} KB\n")

